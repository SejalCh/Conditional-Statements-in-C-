/*exp5_1-To check whether a number is even or odd*/

#include<iostream>
int main()
{
   
    using namespace std;
    int n;
    cout<<"Enter number:";
    cin>>n;
    if(n%2==0)
    {cout<<"It is an even number";}
    else (n%2 !=0);
    {cout<<"It is an odd number ";}
    
}